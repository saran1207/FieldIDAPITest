package com.n4systems.model.signuppackage;

public class CanNotUpgradeException extends RuntimeException {

	public CanNotUpgradeException() {
	}

	public CanNotUpgradeException(String arg0) {
		super(arg0);
	}

	public CanNotUpgradeException(Throwable arg0) {
		super(arg0);
	}

	public CanNotUpgradeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
