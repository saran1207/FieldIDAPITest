package com.n4systems.services.safetyNetwork.exception;

public class ImportFailureException extends Exception {

	private static final long serialVersionUID = 1L;

	public ImportFailureException() {
	}

	public ImportFailureException(String arg0) {
		super(arg0);
	}

	public ImportFailureException(Throwable arg0) {
		super(arg0);
	}

	public ImportFailureException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
