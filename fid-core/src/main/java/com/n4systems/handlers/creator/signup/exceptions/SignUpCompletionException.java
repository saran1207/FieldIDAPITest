package com.n4systems.handlers.creator.signup.exceptions;

public class SignUpCompletionException extends Exception {

	private static final long serialVersionUID = 1L;

	public SignUpCompletionException() {
	}

	public SignUpCompletionException(String arg0) {
		super(arg0);
	}

	public SignUpCompletionException(Throwable arg0) {
		super(arg0);
	}

	public SignUpCompletionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
