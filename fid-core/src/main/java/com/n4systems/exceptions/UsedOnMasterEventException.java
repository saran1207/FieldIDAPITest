package com.n4systems.exceptions;

public class UsedOnMasterEventException extends ArchiveException {

	private static final long serialVersionUID = 1L;

	public UsedOnMasterEventException() {
		super();
	}

	public UsedOnMasterEventException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UsedOnMasterEventException(String arg0) {
		super(arg0);
	}

	public UsedOnMasterEventException(Throwable arg0) {
		super(arg0);
	}

}
