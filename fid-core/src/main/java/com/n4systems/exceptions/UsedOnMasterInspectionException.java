package com.n4systems.exceptions;

public class UsedOnMasterInspectionException extends ArchiveException {

	private static final long serialVersionUID = 1L;

	public UsedOnMasterInspectionException() {
		super();
	}

	public UsedOnMasterInspectionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UsedOnMasterInspectionException(String arg0) {
		super(arg0);
	}

	public UsedOnMasterInspectionException(Throwable arg0) {
		super(arg0);
	}

}
