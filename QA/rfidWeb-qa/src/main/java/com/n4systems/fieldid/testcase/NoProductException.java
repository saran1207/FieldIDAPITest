package com.n4systems.fieldid.testcase;

public class NoProductException extends Exception {

	private static final long serialVersionUID = 2564965061021564096L;

	public NoProductException() {
	}

	public NoProductException(String arg0) {
		super(arg0);
	}

	public NoProductException(Throwable arg0) {
		super(arg0);
	}

	public NoProductException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
