package com.n4systems.fieldid.testcase;

public class NoSubProductException extends Exception {

	private static final long serialVersionUID = 6588577176361028758L;

	public NoSubProductException() {
	}
	
	public NoSubProductException(String arg0) {
		super(arg0);
	}
	
	public NoSubProductException(Throwable arg0) {
		super(arg0);
	}
	
	public NoSubProductException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
