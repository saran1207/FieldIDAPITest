package com.n4systems.exceptions;

public class SubProductArchiveException extends ArchiveException {

	private static final long serialVersionUID = 1L;

	public SubProductArchiveException() {
	}

	public SubProductArchiveException( String arg0 ) {
		super( arg0 );
	}

	public SubProductArchiveException( Throwable arg0 ) {
		super( arg0 );
	}

	public SubProductArchiveException( String arg0, Throwable arg1 ) {
		super( arg0, arg1 );
	}

}
