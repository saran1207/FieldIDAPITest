package com.n4systems.fieldid.actions.helpers;

public class UnbrandedDomainException extends Exception {

	public UnbrandedDomainException() {
	}

	public UnbrandedDomainException(String message) {
		super(message);
	}

	public UnbrandedDomainException(Throwable cause) {
		super(cause);
	}

	public UnbrandedDomainException(String message, Throwable cause) {
		super(message, cause);
	}

}
